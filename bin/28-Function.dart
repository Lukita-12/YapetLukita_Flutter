// Function
void sayHello() {
  print('Hello World!');
}

// Memanggil function
void main() {
  sayHello();
  sayHello();
}

/*
Function adalah blok kode program yang akan berjalan saat kita panggil.
Gunakan kata kunci void, diikuti dengan nama function, kurung () dan diakhiri dengan block.
Memanggil function, gunakan nama function lalu diikuti dengan kurung ().
Di bahasa pemrograman lain, Function juga disebut dengan Method.
*/
